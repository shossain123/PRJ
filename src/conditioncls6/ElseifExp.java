package conditioncls6;

public class ElseifExp {

	public static void main(String[] args) {
		int x;
		x = 2 ;
		if (x>=10) {
			System.out.println("buy burger");
		}
		else if(x==5) {
			System.out.println("buy chocolate");
		}
		else if(x<=3) {
			System.out.println("buy pen");
		}
		else {
			System.out.println("buy bread");
		}

	}

}
